package DataTypes;

public class DataTypesDefining {
    public static void main(String[] args) {
        
        int number = 12;
        boolean hello = true;
        boolean hello1 = false;
        char initial = 's'; // Use 'initial' for better variable naming
        short num = 12;
        long num1 = 12345;
        float floatNumber = 7f;
        double doubleNumber = 1.2;

        System.out.println("int number: " + number);
        System.out.println("boolean hello: " + hello);
        System.out.println("boolean hello1: " + hello1);
        System.out.println("char initial: " + initial); // Use initial here
        System.out.println("short num: " + num);
        System.out.println("long num1: " + num1);
        System.out.println("float floatNumber: " + floatNumber);
        System.out.println("double doubleNumber: " + doubleNumber);
    }
    
}
